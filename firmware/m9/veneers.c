// Read the Secure-side software cache of the clock frequencies. After
// Phase 4 NS skips runtime_init_clocks (CLOCKS/XOSC/PLLs are locked
// Secure-only) so its own clock_get_hz returns 0; this veneer is the
// canonical query path. Caller passes a pico-sdk clock_handle_t (which
// is the small clk_* enum); we pin to uint8_t for the cmse register-arg
// limit and bounds-check before forwarding.
__attribute__((cmse_nonsecure_entry))
uint32_t s_clock_get_hz(uint8_t clock_idx) {
    if (clock_idx >= CLK_COUNT) return 0;
    return clock_get_hz((clock_handle_t)clock_idx);
}

// --- Phase 4: SHA-256 HKDF veneers --------------------------------------
//
// NS's only SHA-256 surface today is HKDF in derive_keys() (common
// SecretConnection). Expose the two HKDF ops only; sha256() and
// hmac_sha256() have no NS callers so they get no veneers. Argument
// structs because cmse_nonsecure_entry caps at 4 register args.
#define M9_HKDF_MAX_LEN 4096u

__attribute__((cmse_nonsecure_entry))
int s_hkdf_extract(const s_hkdf_extract_args_t *args) {
    if (!args) return M9_NEG_PTR;
    const s_hkdf_extract_args_t *a = cmse_check_address_range(
        (void *)args, sizeof(*args), CMSE_NONSECURE | CMSE_MPU_READ);
    if (!a) return M9_NEG_PTR;
    s_hkdf_extract_args_t v = *a;

    if (!v.ikm || !v.prk) return M9_NEG_PTR;
    if (v.ikm_len == 0 || v.ikm_len > M9_HKDF_MAX_LEN) return M9_NEG_RANGE;
    if (v.salt_len > M9_HKDF_MAX_LEN) return M9_NEG_RANGE;

    if (v.salt) {
        if (!cmse_check_address_range((void *)v.salt, v.salt_len,
                                      CMSE_NONSECURE | CMSE_MPU_READ)) return M9_NEG_PTR;
    } else if (v.salt_len != 0) {
        return M9_NEG_PTR;
    }
    if (!cmse_check_address_range((void *)v.ikm, v.ikm_len,
                                  CMSE_NONSECURE | CMSE_MPU_READ)) return M9_NEG_PTR;
    if (!cmse_check_address_range(v.prk, 32,
                                  CMSE_NONSECURE | CMSE_MPU_READWRITE)) return M9_NEG_PTR;

    hkdf_extract(v.salt, v.salt_len, v.ikm, v.ikm_len, v.prk);
    return 0;
}

// --- Phase 7.1: seal/unseal self-test veneer ----------------------------
//
// Bounded NS-visible smoke test for m9_seal_seed / m9_unseal_seed.
// Generates a random 64-byte payload in Secure, seals + unseals with
// the NS-supplied PIN, then re-unseals with a tampered PIN to confirm
// the AEAD tag check fails closed. Mostly there so we can run a
// boot-time sanity check from the TMKMS REPL before relying on the
// primitives for the PIN flow in 7.2.
#define M9_PIN_MIN_LEN 4u
#define M9_PIN_MAX_LEN 16u

__attribute__((cmse_nonsecure_entry))
int s_seal_selftest(const uint8_t *pin, size_t pin_len) {
    if (!pin) return M9_NEG_PTR;
    if (pin_len < M9_PIN_MIN_LEN || pin_len > M9_PIN_MAX_LEN) return -102;
    const void *pin_chk = cmse_check_address_range(
        (void *)pin, pin_len, CMSE_NONSECURE | CMSE_MPU_READ);
    if (!pin_chk) return M9_NEG_PTR;

    // Snapshot the PIN to a local buffer so a TOCTOU race from NS
    // can't change it mid-test.
    uint8_t pin_buf[M9_PIN_MAX_LEN];
    memcpy(pin_buf, pin_chk, pin_len);

    uint8_t plaintext[M9_SEALED_SEED_LEN];
    m9_trng_fill(plaintext, sizeof(plaintext));

    m9_sealed_seed_t blob;
    if (m9_seal_seed(pin_buf, pin_len, plaintext, &blob) != 0) return -1;

    uint8_t round_trip[M9_SEALED_SEED_LEN];
    if (m9_unseal_seed(pin_buf, pin_len, &blob, round_trip) != 0) return -2;
    if (memcmp(round_trip, plaintext, sizeof(plaintext)) != 0) return -3;

    // Flip one bit in the PIN and confirm unseal rejects.
    pin_buf[0] ^= 0x01u;
    if (m9_unseal_seed(pin_buf, pin_len, &blob, round_trip) == 0) return -4;

    // Wipe transient data before returning.
    pin_buf[0] ^= 0x01u;  // restore (cosmetic; we wipe right after)
    crypto_wipe(pin_buf, sizeof(pin_buf));
    crypto_wipe(plaintext, sizeof(plaintext));
    crypto_wipe(round_trip, sizeof(round_trip));
    crypto_wipe(&blob, sizeof(blob));
    return 0;
}

__attribute__((cmse_nonsecure_entry))
int s_hkdf_expand(const s_hkdf_expand_args_t *args) {
    if (!args) return M9_NEG_PTR;
    const s_hkdf_expand_args_t *a = cmse_check_address_range(
        (void *)args, sizeof(*args), CMSE_NONSECURE | CMSE_MPU_READ);
    if (!a) return M9_NEG_PTR;
    s_hkdf_expand_args_t v = *a;

    if (!v.prk || !v.okm) return M9_NEG_PTR;
    if (v.prk_len == 0 || v.prk_len > M9_HKDF_MAX_LEN) return M9_NEG_RANGE;
    if (v.okm_len == 0 || v.okm_len > M9_HKDF_MAX_LEN) return M9_NEG_RANGE;
    // hkdf_expand's stack buffer caps info_len at 128 internally; mirror
    // that here so a bogus NS value can't make us deref into Secure mem.
    if (v.info_len > 128u) return M9_NEG_RANGE;

    if (!cmse_check_address_range((void *)v.prk, v.prk_len,
                                  CMSE_NONSECURE | CMSE_MPU_READ)) return M9_NEG_PTR;
    if (v.info) {
        if (!cmse_check_address_range((void *)v.info, v.info_len,
                                      CMSE_NONSECURE | CMSE_MPU_READ)) return M9_NEG_PTR;
    } else if (v.info_len != 0) {
        return M9_NEG_PTR;
    }
    if (!cmse_check_address_range(v.okm, v.okm_len,
                                  CMSE_NONSECURE | CMSE_MPU_READWRITE)) return M9_NEG_PTR;

    hkdf_expand(v.prk, v.prk_len, v.info, v.info_len, v.okm, v.okm_len);
    return 0;
}

__attribute__((cmse_nonsecure_entry))
int s_flash_write_hwm_page(uint8_t slot_idx, uint16_t page_in_slot,
                           const void *page, size_t len) {
    if (slot_idx >= HWM_TOTAL_SLOTS_LOCAL) return M9_NEG_RANGE;
    if (len != 256u) return M9_NEG_RANGE;        // pico-sdk FLASH_PAGE_SIZE
    uint32_t slot_pages = HWM_BYTES_PER_SLOT / 256u;
    if (page_in_slot >= slot_pages) return M9_NEG_RANGE;
    const void *checked = cmse_check_address_range(
        (void *)page, len, CMSE_NONSECURE | CMSE_MPU_READ);
    if (!checked) return M9_NEG_PTR;
    uint32_t off = HWM_FLASH_OFFSET
                 + (uint32_t)slot_idx * HWM_BYTES_PER_SLOT
                 + (uint32_t)page_in_slot * 256u;
    uint32_t ints = save_and_disable_interrupts();
    flash_range_program(off, (const uint8_t *)checked, len);
    restore_interrupts(ints);
    return 0;
}
